import UIKit

extension Int {
    var cgFloat :CGFloat {
        get {
            return CGFloat(self)
        }
    }
}

extension Double {
    var cgFloat :CGFloat {
        get {
            return CGFloat(self)
        }
    }
}

extension Float {
    var cgFloat :CGFloat {
        get {
            return CGFloat(self)
        }
    }
}
